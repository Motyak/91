import itertools

def gen():
    a = 91
    b = 0
    c = 0
    while True:
        if c // 10 == c % 10:
            a = c + 1
        else:
            a = c * 9 % 91 + 1
        b = a * 9 % 91
        c = b * 9 % 91
        yield (a, b, c)


triplets = [*itertools.islice(gen(), 15)]
for t in triplets:
    print(t)

numbers = [n * 10989 for t in itertools.islice(gen(), 15) for n in t]
for n in numbers:
    print(f"{n:06}")

